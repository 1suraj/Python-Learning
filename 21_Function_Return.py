def add(a,b):
    print(f"adding {a} + {b}")
    return a+b


def substract(a,b):
    print(f"substracting {a} - {b}")
    return a-b


def multiply(a,b):
    print(f"multiplying {a} * {b}")
    return a*b

def division(a,b):
    print(f"division {a} / {b}")
    return a/b



